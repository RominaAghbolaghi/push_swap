#include "libft.h"

int	pf_str(char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		write (1, "(null)", 6);
		return (6);
	}
	else
	{
		while (str[i])
		{
			write (1, &str[i], 1);
			i++;
		}
	}
	return (i);
}
